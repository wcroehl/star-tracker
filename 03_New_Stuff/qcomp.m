function q3 = qcomp(q1, q2)
q3 = zeros(4,1);
q3(1) =  q1(4)*q2(1) + q1(3)*q2(2) - q1(2)*q2(3) + q1(1)*q2(4) ;
q3(2) = -q1(3)*q2(1) + q1(4)*q2(2) + q1(1)*q2(3) + q1(2)*q2(4) ;
q3(3) =  q1(2)*q2(1) - q1(1)*q2(2) + q1(4)*q2(3) + q1(3)*q2(4) ;
q3(4) = -q1(1)*q2(1) - q1(2)*q2(2) - q1(3)*q2(3) + q1(4)*q2(4) ;
end